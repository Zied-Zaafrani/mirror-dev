    "        else:\n"
    "            patience_ctr += 1\n"
    "            if patience_ctr >= patience:\n"
    "                print(f'    Early stop at epoch {epoch}')\n"
    "                break\n"
    "    model.load_state_dict(best_state); model.to(DEVICE)\n"
    "    @torch.no_grad()\n"
    "    def collect_probs(loader):\n"
