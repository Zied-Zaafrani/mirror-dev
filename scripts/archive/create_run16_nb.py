    "        for batch in loader:\n"
    "            diag_seq  = [t.to(DEVICE) for t in batch['diag_seq']]\n"
    "            proc_seq  = [t.to(DEVICE) for t in batch['proc_seq']]\n"
    "            diag_mask = [t.to(DEVICE) for t in batch['diag_mask_seq']]\n"
