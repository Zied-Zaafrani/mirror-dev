    "    print(f'    @{best_val_thresh:.2f}:     Jac={tm_bt[\"Jaccard\"]:.4f} F1={tm_bt[\"F1\"]:.4f} Meds={tm_bt[\"Avg Meds\"]:.1f}')\n"
    "    print(f'    @per-drug: Jac={tm_pd[\"Jaccard\"]:.4f} F1={tm_pd[\"F1\"]:.4f} Meds={tm_pd[\"Avg Meds\"]:.1f}')\n"
